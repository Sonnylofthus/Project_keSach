package com.giang.kesach.models;

import java.io.Serializable;
import java.sql.Date;
import java.util.ArrayList;
import java.util.List;

public class Book {

	private long bId;

	private String bName;

	private String pulisher;

	private int pulishYear;

	private String description;
	//

	private List<Author> authors = new ArrayList<Author>();
	//

	private List<Category> category = new ArrayList<Category>();

	private String imgLink;

	public long getbId() {
		return bId;
	}

	@Override
	public String toString() {
		return "Book [bId=" + bId + ", bName=" + bName + ", pulisher=" + pulisher + ", pulishYear=" + pulishYear
				+ ", description=" + description + ", authors=" + authors + ", category=" + category + "]";
	}

	public void setbId(long bId) {
		this.bId = bId;
	}

	public String getbName() {
		return bName;
	}

	public void setbName(String bName) {
		this.bName = bName;
	}

	public String getPulisher() {
		return pulisher;
	}

	public void setPulisher(String pulisher) {
		this.pulisher = pulisher;
	}

	public int getPulishYear() {
		return pulishYear;
	}

	public void setPulishYear(int pulishYear) {
		this.pulishYear = pulishYear;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public List<Author> getAuthors() {
		return authors;
	}

	public void setAuthors(List<Author> authors) {
		this.authors = authors;
	}

	public List<Category> getCategory() {
		return category;
	}

	public void setCategory(List<Category> category) {
		this.category = category;
	}

	public String getImgLink() {
		return imgLink;
	}

	public void setImgLink(String imgLink) {
		this.imgLink = imgLink;
	}

}
